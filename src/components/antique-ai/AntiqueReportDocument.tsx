function formatDate(locale: Locale, generatedAt?: string) {
  if (generatedAt) return generatedAt;

  const dateLocale =
    locale === "ar" ? "ar-IQ" : locale === "fr" ? "fr-FR" : "en-US";

  return new Intl.DateTimeFormat(dateLocale, {
    year: "numeric",
    month: "short",
    day: "2-digit",
  }).format(new Date());
}

