function formatDate(locale: Locale, generatedAt?: string) {
  if (generatedAt) return generatedAt;

  const dateLocale =
    locale === "ar" ? "ar-IQ" : locale === "fr" ? "fr-FR" : "en-US";

  return new Intl.DateTimeFormat(dateLocale, {
    year: "numeric",
    month: "short",
    day: "2-digit",
  }).format(new Date());
}

function InfoBlock({
  label,
  value,
  strong = false,
}: {
  label: string;
  value?: string;
  strong?: boolean;
}) {
  const cleanValue = cleanReportText(value);

  if (!cleanValue) return null;

  return (
    <div className="rounded-[14px] border border-[#e5d4ba] bg-white/64 p-3">
      <p className="mb-1 text-[8.5px] font-black uppercase tracking-[0.14em] text-[#9a7441]">
        {label}
      </p>
      <p
        className={[
          "whitespace-pre-line break-words leading-[1.65] text-[#241b14]",
          strong
            ? "text-[13.5px] font-black text-[#9a5f18]"
            : "text-[11.2px] font-semibold",
        ].join(" ")}
      >
        {cleanValue}
      </p>
    </div>
  );
}

function TextBox({
  title,
  body,
  compact = false,
}: {
  title: string;
  body?: string;
  compact?: boolean;
}) {
  const cleanBody = cleanReportText(body);

  if (!cleanBody) return null;

  return (
    <section className="rounded-[16px] border border-[#e5d4ba] bg-white/64 p-4">
      <h3 className="mb-2 text-[9px] font-black uppercase tracking-[0.16em] text-[#9a7441]">
        {title}
      </h3>
      <p
        className={[
          "whitespace-pre-line break-words font-semibold text-[#2a2119]",
          compact ? "text-[10.2px] leading-[1.72]" : "text-[11px] leading-[1.82]",
        ].join(" ")}
      >
        {cleanBody}
      </p>
    </section>
  );
}

function ListSection({
  title,
  items,
}: {
  title: string;
  items?: string[];
}) {
  const safeItems = Array.isArray(items)
    ? items.map((item) => cleanReportText(item)).filter(Boolean)
    : [];

  if (safeItems.length === 0) return null;

  if (safeItems.length === 0) {
    return <TextBox title={title} body="—" compact />;
  }

  return (
    <section className="rounded-[16px] border border-[#e5d4ba] bg-white/64 p-4">
      <h3 className="mb-2 text-[9px] font-black uppercase tracking-[0.16em] text-[#9a7441]">
        {title}
      </h3>

      <div className="grid gap-1.5">
        {safeItems.map((item, index) => (
          <div
            key={`${title}-${index}`}
            className="flex gap-2 rounded-[10px] bg-[#f8f1e8] px-2.5 py-1.5 text-[10.2px] font-semibold leading-[1.62] text-[#33251b]"
          >
            <span className="mt-[2px] flex h-4 w-4 shrink-0 items-center justify-center rounded-full bg-[#2a2119] text-[8px] text-[#f5d8a7]">
              {index + 1}
            </span>
            <span className="break-words">{item}</span>
          </div>
        ))}
      </div>
    </section>
  );
}

