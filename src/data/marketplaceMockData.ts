const now = "2026-06-03T00:00:00.000Z";

function image(
  itemId: string,
  imageUrl: string,
  sortOrder = 0,
): MarketplaceItem["images"][number] {
  return {
    id: `${itemId}-image-${sortOrder + 1}`,
    itemId,
    imageUrl,
    storagePath: null,
    sortOrder,
    createdAt: now,
  };
}

